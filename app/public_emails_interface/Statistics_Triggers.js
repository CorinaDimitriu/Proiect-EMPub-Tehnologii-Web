document.getElementById("stats1").addEventListener('mouseenter',function(event) {
    var x  = event.pageX;
    var y  = event.pageY;
    document.getElementById("explanation_stats").style.visibility = "visible";
    document.getElementById("explanation_stats").style.top = `${y}px`;
    document.getElementById("explanation_stats").style.left = `${x+5}px`;
  },false)
  
  document.getElementById("stats1").addEventListener('mouseout',function(event) { 
    document.getElementById("explanation_stats").style.visibility = "hidden";
  },false)

